
package model;

public class Doenca {
    private int id_doencas;
    private String doenca;

    // Getter para id_doencas
    public int getId() {
        return id_doencas;
    }

    // Setter para id_doencas
    public void setId(int id_doencas) {
        this.id_doencas = id_doencas;
    }

    // Getter para doenca
    public String getDoenca() {
        return doenca;
    }

    // Setter para doenca
    public void setDoenca(String doenca) {
        this.doenca = doenca;
    }
}
