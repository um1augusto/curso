    public void atualizarTabelaSintomas() {
        try {
            SintomaDAO sintomaDAO = new SintomaDAO();
            List<Sintoma> sintomas = sintomaDAO.getAllSintomas();

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0); // Remove todas as linhas

            for (Sintoma sintoma : sintomas) {
                model.addRow(new Object[]{sintoma.getId(), sintoma.getSintoma()});
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Erro ao atualizar a tabela de sintomas: " + e.getMessage());
        }
    }
